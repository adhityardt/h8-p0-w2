//1. Melakukan Looping Menggunakan while
console.log('LOOPING PERTAMA');
var counter = 2;
var limit = 20;
var increment = 2;
while (counter <= limit){
  console.log(counter + ' - I love coding');
  counter += increment;
}

console.log('LOOPING KEDUA');
counter -= 2;
while (counter >= 0) {
  console.log(counter + ' - I will become fullstack developer');
  counter -= increment;
}
